def retrieve_cases(q_type, needs=None, k=3):
    """Q유형(+선택적 needs)에 맞는 외부 사례를 점수순으로 검색한다.
    점수 = (Q적합 2점) + (needs 교집합 1점/개). 단순·결정적 검색."""
    needs = set(needs or [])
    scored = []
    for c in CASES:
        s = (2 if q_type in c["fit_q"] else 0) + len(needs & set(c["needs"]))
        if s > 0:
            scored.append((s, c))
    scored.sort(key=lambda x: x[0], reverse=True)
    return [c for _, c in scored[:k]]
