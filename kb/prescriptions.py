# 자치구 Quadrant → 사분면 색
Q_COLORS = {"Q1": "#e74c3c", "Q2": "#e67e22", "Q3": "#3498db", "Q4": "#9b59b6"}


def evaluate_fit(q_type):
    """해당 Q의 제도 후보들을 rubric으로 평가해 적합도 순으로 반환.
    충돌(True) 개수가 적을수록 적합. 결정적(LLM 무관) 로직."""
    q = KB.get(q_type)
    if not q:
        return []
    results = []
    for inst in q["제도후보"]:
        conflicts = [k for k, v in inst.get("rubric", {}).items() if v]
        results.append({
            **inst,
            "_conflicts": conflicts,
            "_score": len(conflicts),   # 낮을수록 적합
            "_fit": "적합" if len(conflicts) == 0 else ("부분 적합" if len(conflicts) <= 1 else "주의 필요"),
        })
    # 회피축이 높은 Q1·Q4는 충돌이 적합도를 좌우 → 충돌 적은 순 정렬
    results.sort(key=lambda r: r["_score"])
    return results


